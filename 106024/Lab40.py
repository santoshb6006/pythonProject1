# Logical operators
a = True
b = False
print(a and b)
print(a or b)
print(not a)
print(not b)
# Logical operators gives always true or false
# These logical operators can be used along with comparison operators as follows
a = 10
b = 20
print(a > b and b > a)
print(a > b or b > a)
print(not a > b)
print(not b > a)
